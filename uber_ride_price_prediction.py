import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns
sns.set_style('darkgrid')

data = pd.read_csv('uber_ride_fare_dataset.csv')

data

data['traffic_level'] = data['traffic_level'].map({'low':0,'medium':1,'high':2})

data

X = data.drop('fare',axis=1)

X

y = data.fare

y

from sklearn.linear_model import LinearRegression

model = LinearRegression()

model.fit(X,y)

model.score(X,y)

model.predict([[1,5,0,40]])

model.predict([[4,15,1,40]])

model.predict([[20,80,3,40]])

